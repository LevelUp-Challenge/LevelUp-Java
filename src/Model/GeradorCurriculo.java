package Model;

public class GeradorCurriculo {
	
	private String objetivoProfissional, resumoCandidato, habilidadesCandidato;

	public String getObjetivoProfissional() {
		return objetivoProfissional;
	}

	public void setObjetivoProfissional(String objetivoProfissional) {
		this.objetivoProfissional = objetivoProfissional;
	}

	public String getResumoCandidato() {
		return resumoCandidato;
	}

	public void setResumoCandidato(String resumoCandidato) {
		this.resumoCandidato = resumoCandidato;
	}

	public String getHabilidadesCandidato() {
		return habilidadesCandidato;
	}

	public void setHabilidadesCandidato(String habilidadesCandidato) {
		this.habilidadesCandidato = habilidadesCandidato;
	}
	
	

}
